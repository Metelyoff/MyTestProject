package javaTuturials;

public class ClassDoWhile {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int count = 1;
		do {
			System.out.println("Count is: " + count);
			count++;
		} while (count < 11);
		System.out.println("------------");
		int counter = 1;
		while (counter < 11) {
			System.out.println("Count is: " + counter);
			counter++;
		}

	}

}
